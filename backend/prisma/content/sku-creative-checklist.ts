function renderSectionTable(section: (typeof skuCreativeChecklistSections)[number]): string {
  const rows = section.items
    .map(
      (item) =>
        `<tr><td>${item.no}</td><td>${item.checkpoint}</td><td></td><td></td><td></td></tr>`
    )
    .join("");
  return `
    <h3>${section.title}</h3>
    <table border="1" cellpadding="8" cellspacing="0" style="width:100%;border-collapse:collapse;margin-bottom:1.5rem">
      <thead><tr><th>No</th><th>Checkpoint</th><th>Status</th><th>Checked</th><th>Sign</th></tr></thead>
      <tbody>${rows}</tbody>
    </table>`;
}

